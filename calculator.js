// Define the numbers and the operation here
let num1 = 3;
let num2 = 5;
let operator = "y";
// Write if-statements here
if (operator === "+") {
  console.log(num1 + num2);
} else if (operator === "-") {
  console.log(num1 - num2);
} else if (operator === "*") {
  console.log(num1 * num2);
} else if (operator === "/") {
  console.log(num1 / num2);
} else console.log("error");
